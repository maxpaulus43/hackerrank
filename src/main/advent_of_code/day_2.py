def checksum(words):
    triples = 0
    doubles = 0

    for word in words:
        char_count = [0] * 26

        for char in word:
            char_count[ord(char) - ord("a")] += 1

        if 2 in char_count:
            doubles += 1
        if 3 in char_count:
            triples += 1

    return triples * doubles


def find_common_letters(words):

    for i in range(len(words[0])):
        changed_words = sorted([word[:i] + word[i + 1:] for word in words])

        for j in range(1, len(changed_words)):
            if changed_words[j] == changed_words[j - 1]:
                return changed_words[j]

    return "none"


