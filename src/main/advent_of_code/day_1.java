    private static int part2(String input) {
        int[] nums = Arrays.stream(input.split("\n")).mapToInt(Integer::parseInt).toArray();

        int sum = 0;
        Set<Integer> m = new HashSet<>();

        int i = 0;
        while (true) {
            sum += nums[i];
            if (m.contains(sum)) {
                return sum;
            } else {
                m.add(sum);
            }
            i = (i + 1) % nums.length;
        }
    }
